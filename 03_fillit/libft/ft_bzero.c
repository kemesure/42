#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptr;
	size_t	n2;

	ptr = s;
	n2 = n;
	++n;
	while (--n + 1 != 0)
		if (n != n2)
			ptr[n] = 0;
}
